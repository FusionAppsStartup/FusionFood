    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        // TODO add your handling code here:
        int linha = jTableSobremesas.getSelectedRow();
        
        Sobremesa s = Modelos.ModelSobremesa.getInstance().listaSobremesa().get(linha);
        try {
            Modelos.ModelSobremesa.getInstance().excluirSobremesa(s);
        } catch (SQLException ex) {
            Logger.getLogger(TelaSobremesa.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TelaSobremesa.class.getName()).log(Level.SEVERE, null, ex);
        }
        Modelos.ModelSobremesa.getInstance().obterListaSobremesa();
        construirTabela();
    }//GEN-LAST:event_jButtonExcluirActionPerformed

