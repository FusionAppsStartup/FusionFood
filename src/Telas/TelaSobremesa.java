    private void construirTabela(){
    ArrayList<Sobremesa> lista = Modelos.ModelSobremesa.getInstance().obterListaSobremesa();
        
        DefaultTableModel modelo = new javax.swing.table.DefaultTableModel();
        modelo.addColumn("Nome");
        modelo.addColumn("Descrição");
        modelo.addColumn("Preço");


        if (lista.size() == 0) {
            modelo.addRow(new String[]{"Sem dados",
                        null,
                        null,});
        }
        
        for (int i = 0; i < lista.size(); i++) {
            Sobremesa s = lista.get(i);
            
            System.out.println("Testando Lista: "+lista.get(i));
            // Alimenta as linhas de dados  
            modelo.addRow(new String[]{
                        s.getNomeSobremesa(),
                        s.getDescricaoSobremesa()+"",
                        "R$ "+formatter.format(s.getPrecoSobremesa())+"",});
        }

        jTableSobremesas.setModel(modelo);

    }

