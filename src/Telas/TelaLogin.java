    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
        // TODO add your handling code here:
        String login = jTextFieldLogin.getText();
        String senha = jPasswordFieldSenha.getText();
        
        Pessoa p = new Pessoa(login, senha);
        p = Modelos.ModelPessoa.getInstance().autenticaPessoa(p);
        
        if(p == null){
//            System.out.println("OBJ NULL");
            JOptionPane.showMessageDialog(this, "Senha ou usuario incorreto!");
        }else{
            Controle.ControleLogin.getInstance().validaPerfil(p);
            JOptionPane.showMessageDialog(null, "Seja Bem-Vindo "+p.getNomePessoa(), "Ol√° "+p.getNomePessoa(), JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        }
    }//GEN-LAST:event_jButtonLoginActionPerformed

